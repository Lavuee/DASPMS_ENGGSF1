class RestockAlert {
    
    // Custom logging function
    private static function writeLog($message) {
        $logDir = __DIR__ . '/../logs';
        if (!is_dir($logDir)) {
            @mkdir($logDir, 0755, true);
        }
        $logFile = $logDir . '/email_debug.log';
        $time = date('Y-m-d H:i:s');
        @file_put_contents($logFile, "[$time] " . $message . PHP_EOL, FILE_APPEND);
    }

